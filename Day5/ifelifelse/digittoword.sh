read -p "Enter a Digit: " D

if [ $D -eq 1 ]
then 
     echo "One"
elif [ $D -eq 2 ]
then
     echo "Two"
elif [ $D -eq 3 ]
then
     echo "Three"
elif [ $D -eq 4 ]
then
     echo "Four"
elif [ $D -eq 5 ]
then
     echo "Five"
elif [ $D -eq 6 ]
then
     echo "Six"
elif [ $D -eq 7 ]
then
     echo "Seven"
elif [ $D -eq 8 ]
then
     echo "Eight"
elif [ $D -eq 9 ]
then
     echo "Nine"
else
     echo "Enter Single Digit Number"
fi

